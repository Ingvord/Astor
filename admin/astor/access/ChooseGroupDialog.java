    public UserGroup getUserGroup() {
        Object  object = groupComboBox.getSelectedItem();
        if (object instanceof UserGroup) {
            return (UserGroup) object;
        }
        else {
            String  name = object.toString();
            if (name.length()==0)
                return null;
            return new UserGroup(name);
        }
    }

